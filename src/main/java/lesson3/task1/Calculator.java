package lesson3.task1;

public class Calculator {
    /*
    Складываемся
     */
    int addition(int numA, int numB) {
        return numA + numB;
    }

    /*
    Вычитаемся
     */
    int subtraction(int numA, int numB) {
        return numA - numB;
    }

    /*
    Размножимся
     */
    int multiplication(int numA, int numB) {
        return numA * numB;
    }

    /*
   Разделимся
    */
    int separation(int numA, int numB) {
        return numA / numB;
    }


}
